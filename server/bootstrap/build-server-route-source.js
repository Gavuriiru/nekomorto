const buildServerRouteSourceFragment = (fragmentName, source = {}) => {
  const keys = SERVER_ROUTE_SOURCE_FRAGMENT_KEYS[fragmentName] || [];
  const fragment = pickDependencyKeys(source, keys);
  return assertRequiredDependencies(
    `buildServerRouteSourceFragments.${fragmentName}`,
    fragment,
    keys,
  );
};

export const buildServerRouteSourceFragments = (...sources) => {
  const merged = mergeDependencySources(...sources);
  return Object.keys(SERVER_ROUTE_SOURCE_FRAGMENT_KEYS).reduce((fragments, fragmentName) => {
    fragments[fragmentName] = buildServerRouteSourceFragment(fragmentName, merged);
    return fragments;
  }, {});
};

export const buildServerRouteContextSource = (...sources) => {
  const merged = mergeDependencySources(...sources);
  assertRequiredDependencies("buildServerRouteContextSource", merged, [
    "app",
    "publicMediaRuntime",
  ]);
  const fragments = buildServerRouteSourceFragments(merged);
  return buildServerRouteDependencySource(
    { app: merged.app },
    fragments.constant,
    fragments.build,
    fragments.can,
    fragments.load,
    fragments.normalize,
    fragments.parse,
    fragments.resolve,
    fragments.write,
    merged.publicMediaRuntime,
    fragments.misc,
  );
};

export default buildServerRouteContextSource;
