const hasOwn = (value, key) => Object.prototype.hasOwnProperty.call(value, key);

export const pickServerRouteSection = (source = {}, keys = []) =>
  keys.reduce((section, key) => {
    if (hasOwn(source, key)) {
      section[key] = source[key];
    }
    return section;
  }, {});

export const createServerRouteSections = (source = {}) =>
  Object.entries(SERVER_ROUTE_SECTION_KEYS).reduce((sections, [sectionName, keys]) => {
    sections[sectionName] = pickServerRouteSection(source, keys);
    return sections;
  }, {});

export default SERVER_ROUTE_SECTION_KEYS;
